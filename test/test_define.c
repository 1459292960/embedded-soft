#include <stdio.h>
#define SECONDE_PER_YEAR (60*60*24*365)
int main()
{
    printf("每年有%u秒 \n",SECONDE_PER_YEAR);
    return 0;
}

/*@在VScode中的输出：
---------------------------------
每年有31536000秒 
---------------------------------
@总结：
秒数用两个字节存储不下，得用4字节存储
*/
